package org.cqut.wzy.osaat.entity.view;

public class IndexInvitationView {

	private String photo;
	private String invitationTitle;
	private String invitationId;
	private String invitationOfPeople;
	private String invitationOfDate;
	private String replyOfPeople;
	private String replyOfTime;
	private int invitationReplyCount;
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	public String getInvitationTitle() {
		return invitationTitle;
	}
	public void setInvitationTitle(String invitationTitle) {
		this.invitationTitle = invitationTitle;
	}
	public String getInvitationId() {
		return invitationId;
	}
	public void setInvitationId(String invitationId) {
		this.invitationId = invitationId;
	}
	public String getInvitationOfPeople() {
		return invitationOfPeople;
	}
	public void setInvitationOfPeople(String invitationOfPeople) {
		this.invitationOfPeople = invitationOfPeople;
	}
	public String getInvitationOfDate() {
		return invitationOfDate;
	}
	public void setInvitationOfDate(String invitationOfDate) {
		this.invitationOfDate = invitationOfDate;
	}
	public String getReplyOfPeople() {
		return replyOfPeople;
	}
	public void setReplyOfPeople(String replyOfPeople) {
		this.replyOfPeople = replyOfPeople;
	}
	public String getReplyOfTime() {
		return replyOfTime;
	}
	public void setReplyOfTime(String replyOfTime) {
		this.replyOfTime = replyOfTime;
	}
	public int getInvitationReplyCount() {
		return invitationReplyCount;
	}
	public void setInvitationReplyCount(int invitationReplyCount) {
		this.invitationReplyCount = invitationReplyCount;
	}
	
}
