package org.cqut.wzy.osaat.entity.view;

public class InvitationReplyView {
	
	private Long replyId;
	private String replyContent;
	private String replyTime;
	private String replyUserName;
	private String replyUserPhoto;
	private Long replyUserIntegral;
	public Long getReplyId() {
		return replyId;
	}
	public void setReplyId(Long replyId) {
		this.replyId = replyId;
	}
	public String getReplyContent() {
		return replyContent;
	}
	public void setReplyContent(String replyContent) {
		this.replyContent = replyContent;
	}
	public String getReplyTime() {
		return replyTime;
	}
	public void setReplyTime(String replyTime) {
		this.replyTime = replyTime;
	}
	public String getReplyUserName() {
		return replyUserName;
	}
	public void setReplyUserName(String replyUserName) {
		this.replyUserName = replyUserName;
	}
	public String getReplyUserPhoto() {
		return replyUserPhoto;
	}
	public void setReplyUserPhoto(String replyUserPhoto) {
		this.replyUserPhoto = replyUserPhoto;
	}
	public Long getReplyUserIntegral() {
		return replyUserIntegral;
	}
	public void setReplyUserIntegral(Long replyUserIntegral) {
		this.replyUserIntegral = replyUserIntegral;
	} 

}
